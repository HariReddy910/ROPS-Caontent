    public class KitchenController : Controller
    {
        string Baseurl = "http://localhost:2159/";
        public async Task<ActionResult> Kitchen()
        {
            List<Kitchen> KitInfo = new List<Kitchen>();
            using (var client = new HttpClient())
            {
                //Passing service base url  
                client.BaseAddress = new Uri(Baseurl);
                client.DefaultRequestHeaders.Clear();
                //Define request data format  
                client.DefaultRequestHeaders.Accept.Add(new MediaTypeWithQualityHeaderValue("application/json"));
                //Sending request to find web api REST service resource GetAllEmployees using HttpClient  
                HttpResponseMessage Res = await client.GetAsync("api/Kitchen");
                //Checking the response is successful or not which is sent using HttpClient  
                if (Res.IsSuccessStatusCode)
                {
                    //Storing the response details recieved from web api   
                    var kitResponse = Res.Content.ReadAsStringAsync().Result;

                    ViewBag.Name = JsonConvert.SerializeObject(kitResponse);
                    //Deserializing the response recieved from web api and storing into the Employee list  
                    KitInfo = JsonConvert.DeserializeObject<List<Kitchen>>(kitResponse);
                }
                List<SelectListItem> Items = new List<SelectListItem>();
                foreach (string sPrinters in System.Drawing.Printing.PrinterSettings.InstalledPrinters)
                {

                    Items.Add(new SelectListItem
                    {
                        Text = sPrinters,
                        Value = sPrinters.ToString()
                    });
                }

                ViewBag.Printer = Items;
            }
            Session["UserModel"] = KitInfo;
            return View(KitInfo);
        }
